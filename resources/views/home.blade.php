          <div class="content">
            <p class="title-subtext">
            Water purification means the process of removing undesirable chemicals, biological contaminants,
             suspended solids, and gases from water.
             The goal is to produce water that is fit for specific purposes. 
             Most water is purified and disinfected for human consumption (drinking water),
              but water purification may also be carried out for a variety of other purposes,
              including medical, pharmacological, chemical, and industrial applications.
               The history of water purification includes a wide variety of methods.
                The methods used include physical processes such as filtration, sedimentation,
                 and distillation; biological processes such as slow sand filters or biologically active carbon;
                  chemical processes such as flocculation and chlorination; 
                  and the use of electromagnetic radiation such as ultraviolet light.
                  Water purification may reduce the concentration of particulate matter including suspended particles,
                   parasites, bacteria, algae, viruses,
                      and fungi as well as reduce the concentration of a range of dissolved and particulate matter.
