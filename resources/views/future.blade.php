        Water-saving is becoming an important global issue as water demand increases but water supply is likely to become less yet to be paid to population growth and climate change. Water maintenance in the urban residential sector is highly related to land use and development density, as well as other socioeconomic and behavioural characteristics of residents.
       Water conservation in the industrial sector has been the most successful partially because of financial incentives to reduce wastewater. Water conservation in the agricultural sector has been slow and has much room for improvement to save water through educational, economic, and policy incentives. Water conservation at one scale may not necessarily save water at another scale. Water is a crucial natural resource without which, there would be no life on Earth. However, 97 per cent of water is in the oceans which are salty and cannot be used for drinking purpose. Of the remaining 2.5 per cent that is freshwater, most is frozen in glaciers and polar ice caps.
        As a result, only less than 1 per cent of the Earth’s water is available for drinking.
        Moreover, the Earth’s water supply is fixed. This is why it is important to conserve and sustainably use it for the health of human society, life support systems and maintaining ecosystem services of the planet earth.
        In India, ensuring water security is a way to ensure food and nutritional security as well as economic security. 
